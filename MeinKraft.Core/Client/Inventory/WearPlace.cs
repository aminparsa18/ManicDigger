
/// <summary>
/// Represents the possible equipment slots a player can wear items in.
/// </summary>
public enum WearPlace
{
    /// <summary>The right hand slot, used for holding blocks or weapons.</summary>
    RightHand = 0,

    /// <summary>The main body armor slot.</summary>
    MainArmor = 1,

    /// <summary>The boots/feet armor slot.</summary>
    Boots = 2,

    /// <summary>The helmet/head armor slot.</summary>
    Helmet = 3,

    /// <summary>The gauntlet/hands armor slot.</summary>
    Gauntlet = 4,
}
